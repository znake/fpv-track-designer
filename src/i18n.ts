function isLanguage(value: string | null): value is Language {
  return value === 'de' || value === 'en'
}

function readStoredLanguage(): Language {
  if (typeof window === 'undefined') return 'de'
  if (typeof window.localStorage.getItem !== 'function') return 'de'
  const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY)
  return isLanguage(stored) ? stored : 'de'
}

function subscribe(onStoreChange: () => void) {
  window.addEventListener(LANGUAGE_CHANGE_EVENT, onStoreChange)
  window.addEventListener('storage', onStoreChange)
  return () => {
    window.removeEventListener(LANGUAGE_CHANGE_EVENT, onStoreChange)
    window.removeEventListener('storage', onStoreChange)
  }
}

function format(template: string, values?: Record<string, string | number>) {
  if (!values) return template
  return Object.entries(values).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, String(value)),
    template,
  )
}

export function setLanguage(language: Language) {
  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language)
  window.dispatchEvent(new Event(LANGUAGE_CHANGE_EVENT))
}

export function useTranslation() {
  const language: Language = useSyncExternalStore(subscribe, readStoredLanguage, () => 'de')

  return {
    language,
    setLanguage,
    toggleLanguage: () => setLanguage(language === 'de' ? 'en' : 'de'),
    t: (key: TranslationKey, values?: Record<string, string | number>) => format(translations[language][key], values),
    gateTypeLabel: (type: GateType) => gateTypeLabels[language][type],
  }
}

export function translateOutsideReact(key: TranslationKey, values?: Record<string, string | number>) {
  return format(translations[readStoredLanguage()][key], values)
}
